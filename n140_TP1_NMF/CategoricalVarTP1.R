################################################
##### Read in the RDS File & Load Packages #####
################################################

library(car)
tp1RDS<-readRDS("/data/jux/BBL/projects/jirsaraieStructuralIrrit/data/n140_TP1_NMF/n140_ARI+DEMO+QA_20180216.rds")

###################################################################################################
##### Create a Copy of the Variable that Sums the Four Irritability Items and Reassign Values #####
###################################################################################################

tp1RDS$IrritabilityCategory<-tp1RDS$IrritabilitySum
tp1RDS$IrritabilityCategory<-recode(tp1RDS$IrritabilityCategory,"c(1,2,3,4)='1'")
tp1RDS$IrritabilityCategory<-as.factor(tp1RDS$IrritabilityCategory)

#########################################################################################################
##### Overwrite the existing RDS and replace it with the new RDS that includes New Categorical Var  #####
#########################################################################################################

saveRDS(tp1RDS, "/data/jux/BBL/projects/jirsaraieStructuralIrrit/data/n140_TP1_NMF/n140_ARI+DEMO+QA_20180531.rds")

